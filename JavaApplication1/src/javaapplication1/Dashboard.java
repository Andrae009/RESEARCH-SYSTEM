    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        try {

            String empId = txtEmId.getText();
            String firstName = txtFirstName.getText();
            String middleName = txtMiddleName.getText();
            String lastName = txtLastName.getText();
            String email = txtEmail.getText();
            String birthday = txtBirthday.getText();
            String age = txtAge.getText();
            String gender = txtGender.getText();
            String tin = txtTin.getText().trim();
            String address = txtAddress.getText();
            String contactNumber = txtContactNumber.getText();
            String pagibig = txtPagIbig.getText();
            String philhealth = txtPhilHealth.getText();

            String url = "jdbc:mysql://127.0.0.1:3306/tism";
            String user = "root";
            String password = "";
            con=DriverManager.getConnection(url, user, password);

            if(btnProcess.getText().equals("Add"))
            {
                sql = "INSERT INTO personal_info(employeeID, firstName, middleName, lastName, email, birthday, age, gender, tin, address, contact_number, pagibig, philhealth) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

                ps = con.prepareStatement(sql);
                ps.setString(1, empId);
                ps.setString(2, firstName);
                ps.setString(3, middleName);
                ps.setString(4, lastName);
                ps.setString(5, email);
                ps.setString(6, birthday);
                ps.setString(7, age);
                ps.setString(8, gender);
                ps.setString(9, tin.isEmpty() ? null: tin);
                ps.setString(10, address);
                ps.setString(11, contactNumber.isEmpty() ? null: contactNumber);
                ps.setString(12, pagibig.isEmpty() ? null: pagibig);
                ps.setString(13, philhealth.isEmpty() ? null: philhealth);

                sql="INSERT INTO educational_background(bachelor_title) VALUES (?)";

                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Successfully added to records!1");

            }
            else if(btnProcess.getText().equals("Update"))
            {
                sql = "UPDATE personal_info SET firstName = ?, middleName = ?, lastName = ?, email = ?, birthday = ?, age = ?, gender = ?, tin = ?, address = ?, contact_number = ?, pagibig = ?, philhealth = ?  WHERE employeeID = ?";

                ps = con.prepareStatement(sql);
                ps.setString(1, firstName);
                ps.setString(2, middleName);
                ps.setString(3, lastName);
                ps.setString(4, email);
                ps.setString(5, birthday);
                ps.setString(6, age.isEmpty() ? null:age);
                ps.setString(7, gender.isEmpty() ? null:gender);
                ps.setString(8, tin.isEmpty() ? null : tin);
                ps.setString(9, address.isEmpty() ? null : address);
                ps.setString(10, contactNumber.isEmpty() ? null : contactNumber);
                ps.setString(11, pagibig.isEmpty() ? null : pagibig);
                ps.setString(12, philhealth.isEmpty() ? null : philhealth);
                ps.setString(13, empId);

                ps.executeUpdate();

                erase();

                JOptionPane.showMessageDialog(this, "Record updated!!");

            }

        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnProcessActionPerformed

