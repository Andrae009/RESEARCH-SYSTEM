    private void checkEmployeeIdPersonal(){
        String empId = txtEmId.getText();
        String query = "SELECT * FROM personal_info WHERE employeeID = ?";
        
        try{
            String url = "jdbc:mysql://127.0.0.1:3306/tism";
            String user = "root";
            String password = "";
            
            con = DriverManager.getConnection(url, user, password);
            
            

            ps = con.prepareStatement(query);
            ps.setString(1, empId);
            rs = ps.executeQuery();
            
            if(rs.next())
            {
                txtFirstName.setText(rs.getString("firstName"));
                txtMiddleName.setText(rs.getString("middleName"));
                txtLastName.setText(rs.getString("lastName"));
                txtEmail.setText(rs.getString("email"));
                txtBirthday.setText(rs.getString("birthday"));
                txtAge.setText(rs.getString("age"));
                txtGender.setText(rs.getString("gender"));
                txtTin.setText(rs.getString("tin"));
                txtAddress.setText(rs.getString("address"));
                txtContactNumber.setText(rs.getString("contact_number"));
                txtPagIbig.setText(rs.getString("pagibig"));
                txtPhilHealth.setText(rs.getString("philhealth"));
                
                
                
                
                btnProcess.setText("Update");
                
            }
            else
            {
                erase();
            }
            
        }catch(SQLException ex)
        {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    
    
    
